package demo;

public class simpleBean {
	private String name = null;
	private int age = 0;

	public simpleBean() {
	}

	public String getName() {
		return name;
	}

	public void setName(String p_name) {
		name = p_name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int p_age) {
		age = p_age;
	}

}
